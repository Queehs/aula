package aula;


public class Calculos {
	public double soma(double i, double n){
		double total = 0;
		total = i + n;
		return total;
	}
	
	public double subtracao(double t, double q){
		double total = 0;
		total = t - q;
		return total;
	}
	
	public double multiplicacao(double h, double c){
		double total = 0;
		total = h * c;
		return total;
	}
	
	public double divisao(double m, double a){
		double total = 0;
		total = m / a;
		return total;
	}

}
